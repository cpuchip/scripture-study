    IF p_status NOT IN ('done', 'dropped', 'in_progress', 'open') THEN
        RAISE EXCEPTION 'complete_todo: invalid status %', p_status;
    END IF;

    -- Resolve ref to id. Try uuid cast first; fall back to slug lookup.
    BEGIN
        v_id := p_ref::uuid;
