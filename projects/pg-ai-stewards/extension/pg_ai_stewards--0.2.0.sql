            SET t.status = $status
            RETURN t
        $$, $1) AS (v agtype)
        $cy$
    USING (jsonb_build_object(
        'id',     v_id::text,
        'status', p_status
    )::text)::ag_catalog.agtype;

    RETURN v_id;
END;
