#!/usr/bin/env node
// publish-book-to-cpuchip.mjs
//
// Copies the scripture-book manuscript PDF into the cpuchip.net repo with a
// version-hashed filename (beyond-the-prompt_<githash>.pdf), and points the
// Teaching page at the new file. Re-run after a new book build to publish a new
// version — old PDFs stay in public/books/ so versions are tracked by filename.
//
// Usage (from the workspace root):  node scripts/publish-book-to-cpuchip.mjs

import { execSync } from 'node:child_process'
import { copyFileSync, mkdirSync, writeFileSync, existsSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const srcPdf = join(root, 'projects/scripture-book/dist/manuscript.pdf')
const bookRepo = join(root, 'projects/scripture-book')
const cpuchip = join(root, 'projects/cpuchip.net')
const outDir = join(cpuchip, 'public/books')
const dataFile = join(cpuchip, 'src/data/book.ts')

if (!existsSync(srcPdf)) {
  console.error(`ERROR: manuscript not found at ${srcPdf}\nBuild the book first (projects/scripture-book).`)
  process.exit(1)
}

const hash = execSync('git rev-parse --short HEAD', { cwd: bookRepo }).toString().trim()
const fileName = `beyond-the-prompt_${hash}.pdf`

mkdirSync(outDir, { recursive: true })
copyFileSync(srcPdf, join(outDir, fileName))
console.log(`Copied manuscript -> public/books/${fileName}`)

mkdirSync(dirname(dataFile), { recursive: true })
writeFileSync(
  dataFile,
  `// Generated by scripts/publish-book-to-cpuchip.mjs — do not edit by hand.\n` +
    `// Re-run that script to publish a new book version.\n` +
    `export const bookPdf = '/books/${fileName}'\n` +
    `export const bookHash = '${hash}'\n`,
)
console.log(`Wrote src/data/book.ts -> bookPdf '/books/${fileName}' (hash ${hash})`)
console.log('Done. Build + commit + push cpuchip.net to ship.')
